// Smart image selection based on product data
export const selectProductImage = (productData) => {
  if (!productData) return getRandomImage();

  const { title = '', description = '', category = '', tags = [], region = '' } = productData;
  const searchText = `${title} ${description} ${category} ${tags.join(' ')}`.toLowerCase();

  // Try region-specific images first
  if (region && REGIONAL_IMAGES[region.toLowerCase().replace(/\s+/g, '_')]) {
    const regionImages = REGIONAL_IMAGES[region.toLowerCase().replace(/\s+/g, '_')];
    return regionImages[Math.floor(Math.random() * regionImages.length)];
  }

  // Try category-specific images
  for (const [categoryName, keywords] of Object.entries(PRODUCT_KEYWORDS)) {
    if (keywords.some(keyword => searchText.includes(keyword))) {
      const categoryImages = PRODUCT_IMAGES[categoryName];
      if (categoryImages && categoryImages.length > 0) {
        return categoryImages[Math.floor(Math.random() * categoryImages.length)];
      }
    }
  }

  // Try direct category match
  const normalizedCategory = category.toLowerCase();
  if (PRODUCT_IMAGES[normalizedCategory]) {
    const categoryImages = PRODUCT_IMAGES[normalizedCategory];
    return categoryImages[Math.floor(Math.random() * categoryImages.length)];
  }

  // Default to textiles (most common)
  return getRandomImage();
};

// Get random image from all categories
export const getRandomImage = () => {
  const allImages = Object.values(PRODUCT_IMAGES).flat();
  return allImages[Math.floor(Math.random() * allImages.length)];
};

// Get multiple images for a product
export const getProductImages = (productData, count = 3) => {
  const images = [];
  const baseImage = selectProductImage(productData);
  images.push(baseImage);

  // Get additional images from the same category
  const category = detectCategory(productData);
  const categoryImages = PRODUCT_IMAGES[category] || PRODUCT_IMAGES.textiles;
  
  for (let i = 1; i < count && i < categoryImages.length; i++) {
    const img = categoryImages[i];
    if (!images.includes(img)) {
      images.push(img);
    }
  }

  // Fill remaining slots with random images
  while (images.length < count) {
    const randomImg = getRandomImage();
    if (!images.includes(randomImg)) {
      images.push(randomImg);
    }
  }

  return images;
};

// Detect category from product data
const detectCategory = (productData) => {
  if (!productData) return 'textiles';

  const { title = '', description = '', category = '', tags = [] } = productData;
  const searchText = `${title} ${description} ${category} ${tags.join(' ')}`.toLowerCase();

  for (const [categoryName, keywords] of Object.entries(PRODUCT_KEYWORDS)) {
    if (keywords.some(keyword => searchText.includes(keyword))) {
      return categoryName;
    }
  }

  return 'textiles'; // Default category
};

